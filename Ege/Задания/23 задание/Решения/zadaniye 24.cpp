#include <iostream>

using namespace std;

int main() {
    int f[34] = { 0 };
    f[10] = 1;
    for (int i = 10; i <= 33; i++) {
        if (i - 1 > 0) {
            f[i] += f[i - 1];
        }
        if (i - 10 > 9) {
            f[i] += f[i - 10];
        }
    }
    cout << f[33];
}
